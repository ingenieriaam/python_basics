# equal and numerical

myBool = 12 == 23
print(myBool)

myBool = 5+5 == 18-8
print(myBool)

myBool = 'white' == 'White'
print(myBool)

myBool = 'white' == 'White'.lower()
print(myBool)

myBool = 100.0 == 100
print(myBool)

myBool = 100.0 != 100
print(myBool)